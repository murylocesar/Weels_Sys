    public void atualizaTabela() {

        try {
            control.rs.first();
            jTxt_codigo.setText(String.valueOf(control.rs.getInt("id_taxa")));

            try {
                jFTxt_dataCad.setText(validar.formDataDateToStr(control.rs.getDate("datacad")));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro na data");
            }
            jTxt_nome.setText(control.rs.getString("descricao"));

            jCbx_parcela.setSelectedItem(control.rs.getString("parcela"));

            jCbx_entrada.setSelectedItem(control.rs.getString("entrada")+"%");

            jFTxt_ano_modelo.setText(control.rs.getString("ano_modelo"));
            if (control.rs.getString("tipo").equals("A")) {
                jCbx_tipo.setSelectedItem("Adicional");
                jTxt_coeficiente.setText(String.valueOf(control.rs.getFloat("coeficiente")));
                jLabel6.setText("coeficiente");
            } else {
                jCbx_tipo.setSelectedItem("Obrigatorio");
                jTxt_coeficiente.setText(String.valueOf(control.rs.getDouble("valor")));
                jLabel6.setText("Valor Taxa");
            }
            jBtt_novo.setEnabled(false);
            jBtt_salvar.setEnabled(false);
            jBtt_editar.setEnabled(true);
            jBtt_excluir.setEnabled(true);
            jBtt_cancelar.setEnabled(true);
            HabilitaDesabilitaCampos(0);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao selecionar os dados do registro!", "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }
