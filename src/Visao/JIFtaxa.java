    public boolean preencherObjeto() {
        mod.setId_taxa(Integer.parseInt(jTxt_codigo.getText()));
        if ("  /  /    ".equals(jFTxt_dataCad.getText())) {
            JOptionPane.showMessageDialog(null, "Digite a data corretamente!", "Erro!", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        mod.setDatacad((java.sql.Date) validar.formDataStrToData(jFTxt_dataCad.getText()));

        char[] salarioErro = jTxt_coeficiente.getText().toCharArray();
        int ok = 0;
        for (int n = jTxt_coeficiente.getText().length() - 1; n > -1; n--) {
            if (salarioErro[n] == '.') {
                ok++;
            }
            if (ok > 1) {
                salarioErro[n] = 'E';
                ok--;
            }
        }

        if ("".equals(jTxt_coeficiente.getText())) {
            JOptionPane.showMessageDialog(null, "Digite o valor ou o coeficiente", "Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        String salario = String.valueOf(salarioErro);
        salario = salario.replaceAll("E", "");

        if ("Adicional".equals(jCbx_tipo.getSelectedItem())) {
            mod.setTipo("A");
            mod.setDescricao("");
            int par = Integer.parseInt((String) jCbx_parcela.getSelectedItem());
            mod.setParcela(par);

            String entrada = (String) jCbx_entrada.getSelectedItem();
            entrada = entrada.replaceAll("%", "");
            int ent = Integer.parseInt(entrada);

            mod.setEntrada(ent);
            mod.setValor(0);
            mod.setCoeficiente(Float.valueOf(salario));
            if ("  /  ".equals(jFTxt_ano_modelo.getText())) {
                JOptionPane.showMessageDialog(null, "Digite o ano do modelo", "Erro", JOptionPane.ERROR_MESSAGE);
                return false;
            }
            mod.setAno_modelo(jFTxt_ano_modelo.getText());
        } else {
            String nomes = jTxt_nome.getText();
            nomes = nomes.replaceAll("  ", " ");
            if ((("".equals(nomes)) || (" ".equals(nomes))) && (!"Adicional".equals(jCbx_tipo.getSelectedItem()))) {
                JOptionPane.showMessageDialog(null, "Preencher o nome!!", "Erro", JOptionPane.ERROR_MESSAGE);
                jTxt_nome.setText("");
                return false;
            }
            mod.setDescricao(nomes);
            mod.setTipo("O");
            mod.setParcela(0);
            mod.setEntrada(0);
            mod.setValor(Double.valueOf(salario));
            mod.setCoeficiente(0);
            mod.setAno_modelo("");
        }

        return true;
    }

    public void preencherTabela(String SQL) {
        ArrayList dados = new ArrayList();
        String[] Colunas = new String[]{"Código", "Descrição", "Valor"};
        control.executaSQL(SQL);
        try {
            control.rs.first();

            do {
                dados.add(new Object[]{control.rs.getInt("id_taxa"), control.rs.getString("descricao"), control.rs.getString("valor")});

            } while (control.rs.next());

        } catch (SQLException ex) {

        }
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTbl_taxaObrigatorias.setModel(modelo);
        jTbl_taxaObrigatorias.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTbl_taxaObrigatorias.getColumnModel().getColumn(0).setResizable(false);
        jTbl_taxaObrigatorias.getColumnModel().getColumn(1).setPreferredWidth(250);
        jTbl_taxaObrigatorias.getColumnModel().getColumn(1).setResizable(false);
        jTbl_taxaObrigatorias.getColumnModel().getColumn(2).setPreferredWidth(121);
        jTbl_taxaObrigatorias.getColumnModel().getColumn(2).setResizable(false);
        jTbl_taxaObrigatorias.getTableHeader().setReorderingAllowed(false);
        jTbl_taxaObrigatorias.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        jTbl_taxaObrigatorias.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }

