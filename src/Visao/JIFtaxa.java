    private void jBtt_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtt_salvarActionPerformed
        if (preencherObjeto()) {
            if (flag == 0) {
                conTaxa.GravarTaxa(mod);
            } else {
                conTaxa.AlterarTaxa(mod);
            }
            jTxt_nome.setEnabled(false);
            jBtt_novo.setEnabled(true);
            jBtt_salvar.setEnabled(false);
            jBtt_editar.setEnabled(false);
            jBtt_excluir.setEnabled(false);
            jBtt_cancelar.setEnabled(false);
            limpaCampos();
            HabilitaDesabilitaCampos(0);
            preencherTabela("select * from tb_taxas where tipo = 'O' order by id_taxa");
            preencherTabelaA("select * from tb_taxas where tipo = 'A' order by id_taxa");
        }
    }//GEN-LAST:event_jBtt_salvarActionPerformed

    private void jBtt_novoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtt_novoActionPerformed

        flag = 0;

        control.Conexao(obj_config._url);
        control.executaSQL("select * from tb_taxas order by id_taxa");

        try {
            if (!control.rs.last()) {
                jTxt_codigo.setText("1");
            } else {
                jTxt_codigo.setText(String.valueOf(control.rs.getInt("id_taxa") + 1));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao buscar o último código!\n\n" + ex, "Erro", JOptionPane.ERROR_MESSAGE);
        }
        java.util.Date datahoje = new java.util.Date();
        SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        jFTxt_dataCad.setText(df.format(datahoje));
        jBtt_novo.setEnabled(false);
        jBtt_salvar.setEnabled(true);
        jBtt_editar.setEnabled(false);
        jBtt_excluir.setEnabled(false);
        jBtt_cancelar.setEnabled(true);
        HabilitaDesabilitaCampos(1);
        jTxt_nome.setEnabled(false);
        jFTxt_ano_modelo.setEnabled(true);
    }//GEN-LAST:event_jBtt_novoActionPerformed

    private void jTbl_taxaObrigatoriasKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTbl_taxaObrigatoriasKeyReleased
        if (evt.getKeyCode() == KeyEvent.VK_DOWN || evt.getKeyCode() == KeyEvent.VK_UP) {
            String codigo_taxa = "" + jTbl_taxaObrigatorias.getValueAt(jTbl_taxaObrigatorias.getSelectedRow(), 0);
            control.Conexao(obj_config._url);
            control.executaSQL("select * from tb_taxas where id_taxa = '" + codigo_taxa + "'");
            atualizaTabela();
        }
    }//GEN-LAST:event_jTbl_taxaObrigatoriasKeyReleased

