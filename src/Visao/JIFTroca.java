    public boolean preencherObjeto() {

        model.setId_troca(Integer.parseInt(jTxt_codigo.getText()));
        model.setDatacad((Date) validar.formDataStrToData(jFTxt_datacad.getText()));

        if ("".equals(jTxt_valor_venda.getText())) {
            JOptionPane.showMessageDialog(null, "Complete o campo valor vendido", "Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        model.setCliente((String) (jCbx_Clientes.getSelectedItem()));
        model.setFuncionario((String) (jCbx_Funcionarios.getSelectedItem()));

        try {
            model.setId_veiculo((int) jTbl_Veiculos_saida.getValueAt(jTbl_Veiculos_saida.getSelectedRow(), 0));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "selcione o veiculo da saida", "Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        try {
            model.setId_veiculo_entrada(Integer.parseInt(jTxt_codigo_Vei.getText()));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex, "Erro", JOptionPane.ERROR_MESSAGE);
        }

        model.setForma_pagamento((String) jCbx_formaPagamento.getSelectedItem());

        if (("".equals(jTxt_valor_venda.getText()))) {
            JOptionPane.showMessageDialog(null, "Erro no Valor troca!", "Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        model.setValor_troca(Double.valueOf(jTxt_valor_venda.getText().replaceAll("\\.", "").replaceAll("\\,", ".")));

        if (jCbx_formaPagamento.getSelectedItem() == "Financiado") {

            if (("".equals(jTxt_financiado.getText()))) {
                JOptionPane.showMessageDialog(null, "Erro no Valor venda!", "Erro", JOptionPane.ERROR_MESSAGE);
                return false;
            }
            model.setValor_financiado(Double.valueOf(jTxt_valor_venda.getText().replaceAll("\\.", "").replaceAll("\\,", ".")));

            model.setBanco((String) jCbx_banco.getSelectedItem());
        } else {
            model.setValor_financiado(0.0);
            model.setBanco("");
        }

        if ("".equals(jTxt_garantia.getText())) {
            JOptionPane.showMessageDialog(null, "Complete o campo de garantia", "Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        int garantia = Integer.parseInt(jTxt_garantia.getText());
        model.setTempo_garantia(garantia);
        if ("Mês".equals((String) jCbx_garantia.getSelectedItem())) {
            model.setTipo_garantia("Mes");
        } else {
            model.setTipo_garantia((String) jCbx_garantia.getSelectedItem());
        }
        model.setNulo("N");

        mod.setId_veiculo(Integer.parseInt(jTxt_codigo_Vei.getText()));

        mod.setDataCad((Date) validar.formDataStrToData(jFTxt_datacad.getText()));

        mod.setCompra_venda("T");

        if (jCbx_veiculo.getSelectedItem() == "Moto") {
            mod.setVeiculo("M");
        } else if (jCbx_veiculo.getSelectedItem() == "Carro") {
            mod.setVeiculo("C");
        } else {
            mod.setVeiculo("O");
        }
        if (jCbx_condicao.getSelectedItem() == "Novo") {
            mod.setCondicao("N");
        } else if (jCbx_veiculo.getSelectedItem() == "Semi") {
            mod.setCondicao("S");
        } else {
            mod.setCondicao("U");
        }
        if ("".equals(jTxt_quilometro.getText())) {
            JOptionPane.showMessageDialog(null, "Complete o campo quilomentragens", "Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        mod.setQuilomentragem(Integer.parseInt(jTxt_quilometro.getText()));
        if ("  /  /    ".equals(jFTxt_dataFabricacao.getText())) {
            JOptionPane.showMessageDialog(null, "Digite a data de fabricação!", "Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        mod.setDataFab((Date) validar.formDataStrToData(jFTxt_dataFabricacao.getText()));
        mod.setAnoModelo(jFTxt_anoModelo.getText());
        if (jCbx_porte.getSelectedItem() == "Pequeno") {
            mod.setPorte("P");
        } else if (jCbx_porte.getSelectedItem() == "Medio") {
            mod.setPorte("M");
        } else {
            mod.setPorte("G");
        }
        mod.setMarca((String) jCbx_marca.getSelectedItem());
        mod.setModelo((String) jCbx_modelo.getSelectedItem());
        mod.setVersao(jTxt_versao.getText());
        if (!validar.chassi(jTxt_chassi.getText())) {
            JOptionPane.showMessageDialog(null, "Chassi invalido", "Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        mod.setChassi(jTxt_chassi.getText());
        mod.setPlaca(jFTxt_placa.getText());
        mod.setCor((String) jCbx_cor.getSelectedItem());
        int porta = Integer.parseInt((String) (jCbx_porta.getSelectedItem()));
        mod.setPorta(porta);
        if (jRBt_flex.isSelected()) {
            mod.setFlex("T");
        } else {
            mod.setFlex("F");
        }
        if (jRBt_direcao.isSelected()) {
            mod.setDirecao_hidraulica("T");
        } else {
            mod.setDirecao_hidraulica("F");
        }
        if (jRBt_arC.isSelected()) {
            mod.setAr_condicionado("T");
        } else {
            mod.setAr_condicionado("F");
        }

        mod.setObs(jTxt_obs.getText());

        if (("".equals(jTxt_valor_venda.getText()))) {
            JOptionPane.showMessageDialog(null, "Erro no Valor pago do veiculo!", "Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        mod.setValor_pago(Double.valueOf(jTxt_valor_pago.getText().replaceAll("\\.", "").replaceAll("\\,", ".")));

        if (("".equals(jTxt_valor_venda.getText()))) {
            JOptionPane.showMessageDialog(null, "Erro no Valor pago do veiculo!", "Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        mod.setValor_venda(Double.valueOf(jTxt_valor_venda.getText().replaceAll("\\.", "").replaceAll("\\,", ".")));

        mod.setSituacao("T");

        return true;
    }

