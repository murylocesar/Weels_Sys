    void Manual() {
        if (Manual <= 0) {

            Manual = 0;
            numero.setText("P치gina: 1");

            proximo.setEnabled(true);
            anterior.setEnabled(false);
            imagem.setIcon(new ImageIcon(new ImageIcon(getClass().getResource("/Imagens/Manual.PNG")).getImage().getScaledInstance(784, 684, Image.SCALE_DEFAULT)));

        } else if (Manual == 1) {

            numero.setText("P치gina: 2");
            proximo.setEnabled(true);
            anterior.setEnabled(true);

            imagem.setIcon(new ImageIcon(new ImageIcon(getClass().getResource("/Imagens/Manual1.PNG")).getImage().getScaledInstance(784, 651, Image.SCALE_DEFAULT)));
        } else if (Manual == 2) {

            numero.setText("P치gina: 3");
            proximo.setEnabled(true);
            anterior.setEnabled(true);

            imagem.setIcon(new ImageIcon(new ImageIcon(getClass().getResource("/Imagens/Manual2.PNG")).getImage().getScaledInstance(784, 684, Image.SCALE_DEFAULT)));
        } else if (Manual >= 3) {

            numero.setText("P치gina: 4");
            proximo.setEnabled(false);
            anterior.setEnabled(true);

            Manual = 3;
            imagem.setIcon(new ImageIcon(new ImageIcon(getClass().getResource("/Imagens/Manual3.PNG")).getImage().getScaledInstance(784, 650, Image.SCALE_DEFAULT)));
        }

    }

